package com.cisco.gradle;

public class Calculator {
    public int integerAddition(int addend1, int addend2) {
        return addend1 + addend2;
    }
    public int integerSubtraction(int minuend, int subtrahend) {
        return minuend - subtrahend;
    }
    public int integerMultiplication(int factor1, int factor2) {
        return factor1 * factor2;
    }
    public int integerDivision(int divident,int divisor){
        return divident/divisor;
    }

}
